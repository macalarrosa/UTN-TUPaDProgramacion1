# Escribir un programa que solicite su nota al usuario. Si la nota es mayor o igual a 6, deberámostrar por pantalla un mensaje que diga “Aprobado”; en caso contrario deberá mostrar el mensaje “Desaprobado”#
nota = int(input("Ingrese su nota por favor:"))
if nota >= 6 :
    print ("APROBADO")
else: 
    print ("DESAPROBADO")